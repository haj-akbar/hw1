#Holidays in Canada!!
month = input("Enter your desired month : ")
day = int(input("Enter the day : "))
if (month == "january") and (day == 1):
    print("new years day")
elif (month == "july") and (day == 1):   
    print("canada day")
elif (month == "december") and (day == 25):
    print("christmas day")    
else:
    print("work day")    
     